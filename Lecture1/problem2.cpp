#include <iostream>
using namespace std;


int main(){
    int a = 36, b = 6;

    cout << "The sum is " << a + b << endl;
    cout << "The difference is " << a - b << endl;
    cout << "The product is " << a * b << endl;
    cout << "The quotient is " << a / b << endl;
    cout << "The reminder is " << a % b << endl;
    return 0;

}

